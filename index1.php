            <h1>A Guide to Recycling Plastics</h1>



              <h3>Sorting and preparing your recycling!</h3>
            
                <p> 1. Empty and rinse
                    Please rinse out all your containers to remove liquids and food scraps. Dirty recyclables cannot be recycled and will go to landfill (plus they will make your bin smell).</p>
            
                <p> 2. Leave the recycling loose
                    Place all recyclable materials into your recycling bin loosely - do not stack or sort items into boxes or plastic bags. As recyclables cannot be separated on the sorting line and plastic bags clog up the machinery so cannot be recycled.</p>
               
                <p> 3. Leave lids and labels 
                    Lids from plastic containers can be recycled so place them in the bin as well. </p>
            
                <p> 4. Check the number in the triangle symbol and recycle clean plastic containers which have the number 1, 2 or 5.</p>
                
                <p> 5. Items made of plastic and paper (combined materials) can not be recycled, such as plastic-coated coffee cups, laminated paper etc.</p>

                
                <p> 6. Flatten or tear cardboard boxes to save space.</p>
                
   
            
            
            
           

            <h1> Why should I recycle? </h1>

            <p>Recovery of waste plastic for reuse into new products conserves non-renewable raw materials, uses less energy than that used to produce new plastic and creates employment and businesses opportunities. </p> 
            
            <p>Recycling reduces the need to take raw materials from our fragile environment and saves energy as it is increasingly important to use resources efficiently. </p>
            
            <p>Protects ecosystems and wildlife as fewer trees are cut down, rivers diverted, wild animals harmed or displaced, and less pollution of water, soil and air. </p>
            
            <p>Every time products are reused or recycled, it is rereducing the amount of waste going to landfill, helping to reduce economic cost.</p>
            

            <h1> How can I reduce my rubbish?</h1>   
            
                <ul>
                    <li>Garden waste should go in the green waste bin and do not put lawn clippings or branches in the recycling bin.</li>
                    <li>Compost food scraps which can be used as fertiliser for gardens. </li>
                    <li>Electronic waste, utilise the services of electronic retailers who have recycling bins for discarded appliances (mobile phones, batteries etc).</li>
                    <li>Reduce paper waste by stopping unwanted paper mail for subcription and switcing to online methods for bills etc. </li>
                    <li> Knowledge is power! Not sure if it is recycable sure it up and find the proper way to dispose of the product. Products like paint cans, batteries, smoke alarms (radioactive waste) can be tricky.</li>
                </ul>
            
