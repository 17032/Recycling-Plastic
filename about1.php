            <h3> What are the types of plastic?</h3>
            
                <p>There are 7 types of plastic:</p>

                <p> 1) Polyethylene Terephthalate (PETE) </p>
                <ul><li> What products is it used in? Clear bottles, food trays (clear, green, black etc.)</li></ul>
                <ul><li> What does it look like? A tough plastic that discolours if you bend it.</li></ul> 


                <p> 2) High-Density Polyethylene (HDPE)</p>
                <ul><li> What products is it used in? White milk bottles, bleach type bottles, washing machine liquids and some bottle caps.</li> </ul>
                <ul><li> What does it look like? A thick touch plastic that will spring back if bent, caps can usually be flexed. </li></ul> 
            

            
                <p> 3) Polyvinyl Chloride (PVC) </p>
                <ul><li> What products is it used in? Clear bottles (look for a line on the bottom of the bottle), food trays, toys, piping, wire insulation.</li></ul>
                <ul><li> What does it look like? A fragile plastic will crack, bottles make a 'crinkle' cracking sound if squeezed.</li></ul> 

                <p> 4) Low-Density Polyethylene (LDPE) </p>
                <ul><li> What products is it used in? Plastic bags, plastic wrapping, cling film.</li></ul>
                <ul><li> What does it look like? Can be very thin to thick, but usually flexible and easily. </li></ul> 

                
                <p> 5) Polypropylene (PP)</p>
                <ul><li> What products is it used in? Butter tubs, clear fresh soup containers, some bottle caps, glass jar caps.</li></ul>
                <ul><li> What does it look like? Will shatter into stripes if compressed, caps will usually be too hard to flex.</li></ul> 
        

                <p> 6) Polystyrene or Styrofoam (PS)</p>
                <ul><li>What products is it used in? Yoghurt pots, insulated disposable cups, some trays, parcel packaging.</li></ul>
                <ul><li> What does it look like? Will tear or pull apart depending on the form.</li></ul> 
                

                <p> 7) Other</p>
                <ul><li>What products is it used in? Reading glasses, CDs and DVDs and cases, some electrical connections and wiring, general household plastics. </li></ul>

